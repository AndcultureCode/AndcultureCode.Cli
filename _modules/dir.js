const shell = require("shelljs");


/**************************************************************************************************
 * Functions
 **************************************************************************************************/

// #region Dir commands

const dir = {
    popd(dir) {
        shell.popd("-q", dir);
    },
    pushd(dir) {
        shell.pushd("-q", dir);
    }
}

// #endregion Dir commands


/**************************************************************************************************
 * Exports
 **************************************************************************************************/

module.exports = dir;